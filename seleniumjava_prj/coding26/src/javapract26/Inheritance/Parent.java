package javapract26.Inheritance;

public class Parent {
	static void one()
	{
		System.out.println("Parents static mthd-P1");
	}
	static void two()
	{
	System.out.println("Parents static mthd not overridden-P2");
	}
void three()
{
	System.out.println("Parents non static mthd-P3");
}
void four()
{
	System.out.println("Parents non static mthd not overridden-P4");
}
}
